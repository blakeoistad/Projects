import UIKit

extension UIColor {
    
    class func darkRedColor() -> UIColor {
        return UIColor(red: 142.0/255.0, green: 23.0/255.0, blue: 30.0/255.0, alpha: 1.0)
    }
    
    class func orangishColor() -> UIColor {
        return UIColor(red: 226.0/255.0, green: 80.0/255.0, blue: 38.0/255.0, alpha: 1.0)
    }
    
    class func lightOrangeColor() -> UIColor {
        return UIColor(red: 246.0/255.0, green: 146.0/255.0, blue: 87.0/255.0, alpha: 1.0)
    }
    
    class func creamyOrangeColor() -> UIColor {
        return UIColor(red: 246.0/255.0, green: 204.0/255.0, blue: 174.0/255.0, alpha: 1.0)
    }
    
}
